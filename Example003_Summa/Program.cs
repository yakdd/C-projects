// exmp_003 Summa
int numberA = new Random().Next(1, 10);     // [1,10)
int numberB = new Random().Next(1, 10);
Console.WriteLine(numberA + "+" + numberB + "=" + numberA + numberB);   // 3+6=36
int summ = numberA + numberB;
Console.WriteLine(numberA + "+" + numberB + "=" + summ);                // 3+6=9
